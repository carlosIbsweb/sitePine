<p><h5 style="color: #274033;">Vestuário e itens de proteção individuais</h5></p>
As crianças devem vir:<br/>
•  Com roupas adequadas para as condições de temperatura e precipitação do dia;<br/>
•  Calçando sapatos fechados, de preferência tênis ou botinas;<br/>
•  Utilizando máscaras faciais;<br/>
•  Protegidas com protetor solar e repelente de insetos.<br/>

<p><h5 style="color: #274033;">Mochila</h5></p>
A mochila deve conter:<br/>
•  Duas trocas de roupa;<br/>
•  Um par de sapato extra;<br/>
•  Máscaras adicionais;<br/>
•  Roupa de banho;<br/>
•  Toalha;<br/>
•  Garrafa de água identificada com o nome da criança;<br/>
•  Protetor solar identificado;<br/>
•  Repelente de insetos identificado;<br/>
•  Itens de higiene (escova, pasta dental, sabonete...).
