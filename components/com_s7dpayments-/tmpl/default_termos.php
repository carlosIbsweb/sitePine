<p><h4 style="color: #274033;">Termo de adesão e condições para a participação na
Colônia de Férias Pine Tree Farm
Inverno de 2021</h4></p>
<p><h5 style="color: #274033;">Datas</h5>
Entre os dias 05 a 09 e 12 a 16 de julho de 2021.</p>

<p><h5 style="color: #274033;">Turnos e valores semanais</h5>
•  Matutino - 8h às 12h: R$ <br/>
•  Soneca - 9h às 13h (com almoço): R$ <br/>
•  Semi integral – 8h às 16h (com almoço): R$ <br/>
•  Integral – 8h às 18h (com almoço): R$ <br/>
•  Vespertino – 14h às 18h: R$</p> 

<p>Todos os turnos incluem lanches matutinos e/ou vespertinos, servidos respectivamente às 9h30 e 15h30. Haverá descontos regressivos para inscrições antecipadas e descontos progressivos para irmãos.</p>
<p>
As vagas são limitadas.</p>

<p><h5 style="color: #274033;"><span style="text-align:center">Política de Cancelamento</span></h5></p>
<p>Em função da pandemia de Covid 19, a <b>Pine Tree Farm</b> guarda o direito, até o dia 25 de junho de 2021, de cancelar a Colônia de Férias Inverno 2021, aplicando às famílias contratantes reembolso integral do valor pago em até 30 dias, sem e aplicação de multas, atualização monetária, incidência de juros ou qualquer outra penalidade.</p>
<p>Até o dia 25 de junho de 2021, as famílias também poderão cancelar as inscrições realizadas na <b>Colônia de Férias Pine Tree Farm</b>, Inverno 2021 sem e aplicação de multas, atualização monetária, incidência de juros ou qualquer outra penalidade, com reembolso integral do valor pago em até 30 dias.</p>
<p>Na eventualidade de cancelamento ou desistência após o dia 25 de junho de 2021, o reembolso, também em até 30 dias, será de 75% do valor pago ou não utilizado.</p>

<p><h5 style="color: #274033;">Protocolos Sanitárias</h5></p>
<p>•  O uso de máscara será obrigatório.<br/>
•  Todas as crianças terão sua temperatura corporal aferida quando da chegada.<br/>
•  As crianças higienizarão suas mãos com água e sabão entre atividades.<br/>
•  Na eventualidade de qualquer criança que apresentar qualquer sintoma de COVID-19, entraremos em contato imediato com a família para que possa ser avaliada por um médico.<br/>
•  Haverá pulverizadores de álcool 70% líquido em todos os ambientes.<br/>
•  As crianças permanecerão em seus grupos, evitando o contato entre grupos.<br/>
•  A higienização de ambientes será reforçada.</p> 

